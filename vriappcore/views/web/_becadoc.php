                 <h1>BECA DOCENTE UNIVERSITARIO
</h1>
                 <section>
           El Ministerio de Educación (MINEDU), a través del Programa Nacional de Becas y Crédito Educativo (PRONABEC), ha anunciado la Convocatoria 2017 de la Beca Docente Universitario, la cual tiene como objetivo contribuir con la reducción de la brecha de docentes de universidad pública que no cuentan con el grado académico minlmo requerido para la enseñanza. <br><br>

Cabe señalar que la Dirección General de Educación Superior Universitaria participó en el diseño y actualmente apoya en la implementación de la convocatoria de la beca en mención, con énfasis en su difusión para lograr que los docentes cuenten con toda la información pertinente a fin de promover su postulación. <br><br>

En la presente convocatoria se otorgarán 160 becas para estudios de maestria, las cuales brindarán apoyo económico que cubrirán los siguientes conceptos:
 
<ul>
<li>(i) Costo académico del programa de maestrla.
<li>(ii) Fondo para la elaboración de la Tesis.
<li>(iii) Manutención para cubrir los gastos de alimentación, alojamiento y servicios.
<li>(iv) Materiales de estudio.
<li>(v) Movilidad local.
<li>(vi) Pasaje interprovincial.
</ul>


Asimismo, se precisa que los beneficios señalados en los ítems i), ii), y iv) aplican para todos los becarios; sin embargo, los conceptos de Manutención y Pasaje interprovincial, serán proporcionados únicamente a los becarios que se desplacen fuera de sus regiones de origen para cursar el programa de maestría. Respecto a Movilidad local, este beneficio será otorgado de manera exclusiva a los becarios que realicen sus estudios en su misma región de origen.



                
