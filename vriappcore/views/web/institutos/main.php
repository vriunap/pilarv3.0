                 <h1>FINANCIAMIENTO PARA PROYECTOS DE INVESTIGACIÓN DE INSTITUTOS DE INVESTIGACIÓN E</h1>
                 <section>
                  El Vicerrectorado de Investigación de la UNA-PUNO convoca al Concurso para el financiamiento de proyectos de investigación de los Institutos de Investigación de la UNA-PUNO en sus diferentes áreas de investigación: Ciencias de la Ingeniería, Ciencias Biomédicas, Ciencias Sociales y Ciencias Económico Empresariales.

                  A efectos del presente concurso, es necesario que los integrantes de cada Instituto de Investigación formulen y elaboren un proyecto de investigación priorizando una de sus líneas de investigación. El proyecto de investigación sometido a concurso debe estar elaborado de acuerdo con los estándares internacionales y la exigencia de las revistas indizadas donde se publicarán sus resultados.

                  Los miembros integrantes de los institutos de investigación ganadores deberán obligatoriamente escribir y presentar anualmente, sus resultados de investigación en uno o más artículos científicos en la base de datos Thomson- Reuters (ex ISI), con afiliación de la UNA-PUNO y de sus respectivos institutos. Los fondos concursables del presente proyecto no financiarán proyectos de creación artística, recopilaciones, confección de catálogos o inventarios, impresión de libros, ensayos, traducciones, audiovisuales, textos de enseñanza u otras actividades análogas. 
                 </section>

                <h2>Requisitos</h2>
                
                <section>
                    <li>Formulario donde se indique el nombre del Instituto de Investigación, su clasificación y los integrantes de este con sus respectivos cargos o ponsabilidades. (Se recomienda contar para cada integrante del Instituto con la certificación de conducta responsable en investigación de CONCYTEC)
                    </li> 
                    <li>Resolución Rectoral o de Decanato de creación o propuesta de creación del Instituto de Investigación. </li>
                    <li>Formulario donde se indique la línea de investigación en la cual propondrán y desarrollarán el proyecto de investigación elegido por el Instituto de Investigación. </li>
                    <li>Reglamento interno de funcionamiento del instituto. </li>
                    <li>Un archivo en PDF (Anexo 2) del proyecto de investigación del Instituto de Investigación, en un máximo de 15 páginas correspondiente a una ea de investigación seleccionado por sus miembros, considerando los siguientes ítems: </li>
                        <ul>
                          <li> Título del proyecto de investigación concursable </li>
                          <li> Autores  </li>
                          <li> Resumen y palabras clave (una página) </li>
                          <li> Planteamiento del problema de investigación </li>
                          <li> Introducción </li>
                          <li> Hipótesis de trabajo </li>
                          <li> Objetivos </li>
                          <li> Metodología </li>
                          <li> Cronograma de actividades </li>
                          <li> Presupuesto </li>
                        </ul> 
                    <li> Referencias </li>
                    <li> Anexos (Si los hubiese)  </li>

                </section>

                <h2>Inscripción</h2>
                
                <section>
                <ul>
                   <li>Vía Plataforma PILAR y comprenderá dos etapas: Cumplimiento de requisitos y evaluación de este</li>
                  
                </ul>   

                </section>

                <h2>Financiamiento a ganadores</h2>
                
                <section>
                 
                       Se financiarán proyectos de investigación correspondientes a una línea de investigación de nueve (09) institutos de investigación de la UNA-PUNO. Los recursos solicitados deberán guardar estrecha relación con la normatividad legal señalada en el Inciso I del Reglamento. Los objetivos, actividades y el plan de trabajo propuesto no podrán exceder la suma de S/. 120,000.00 (Ciento veinte mil y 00/100 Nuevos Soles). 
                </section>



