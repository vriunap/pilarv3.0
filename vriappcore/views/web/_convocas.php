                 <h1>TESIS EN TRES MINUTOS 
                 (3MT® UNA-PUNO)</h1>
                 <section>
                <i>Tesis en Tres Minutos UNA-Puno (3MT®)</i>, es una competencia de comunicación de la investigación, donde se desafía a los estudiantes de pregrado y graduados a comunicar, sólo en tres minutos y en idioma inglés, la significancia de sus proyectos de tesis a una audiencia no especializada. Esto implica una presentación oral, en el que los expositores deberán explicar de forma convincente, concisa y clara su investigación, en un lenguaje apropiado para un público no especialista en sus temas.

                Este concurso, en su nombre original, Three Minutes Thesis 3MT® ha sido desarrollado por la Universidad de Queensland (UQ) Australia, desde el año 2008, debido a su éxito ha sido replicado en varias otras universidades y ha dado origen a una versión internacional e interinstitucional del mismo concurso.
                 </section>

