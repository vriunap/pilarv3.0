            * La presentación de artículos de investigación deben regirse a las Normas de Publicación de la
            <a target="_blank" href="http://huajsapata.unap.edu.pe/ria/"> Revista de Investigaciones Altoandinas </a>.
            <br>El formato de redacción debe tener la apariencia que muestra <a href="http://huajsapata.unap.edu.pe/ria/index.php/ria/article/view/476/384" target="_blank">este artículo de ejemplo</a>

            <hr>
            <b>Contenido del manuscrito a enviar:</b>
            <hr>
            <p>
                <b>&bullet; Título del Artículo:</b>
                Se redacta en español e inglés en promedio de 15 palabras, debe ser atractivo, innovador, preciso, claro y expresar el contenido del escrito; no utilizar nombres comerciales ni abreviaturas, ya que podrían tener diferentes significados en diferentes campos. Por ejemplo "Ca" de calcio podría ser confundido con "CA", que significa el cáncer.
            </p>

            <p>
                <b>Autor (es) y afiliación:</b>
                consignar nombres y apellidos completos de cada uno de los autores, la afiliación institucional, incluido ciudad y el País (en caso de universidades, consignar el Departamento Académico - Facultad, Universidad, ciudad y País). La consignación debe estar expresados en súper índice numerada correlativamente y con asterismo para autor de correspondencia (email). En caso de más un autor, es necesario adjuntar resolución o documento que autorice o disponga la ejecución de investigación corporativa o documento firmado por el funcionario responsable plausible de verificación (sellado y firmado) o de justificar sustantivamente la aportación original del equipo, dado que se tendrá muy presente en la estimación del manuscrito (Máximo se aceptarán cinco autores para cada manuscrito). Es necesario tener pleno conocimiento de Conducta Responsable de Investigación. Al final, consignar el número ORCID de cada autor (registrado https://orcid.org/).
            </p>

            <p>
                <b> </b>
                Resumen y Abstract redactado en español e inglés, debe ser puntual escrito en tiempo pretérito y un sólo párrafo en un máximo de 250 palabras, ya que, se refiere a un trabajo ya realizado. Incluye introducción, materiales y métodos, resultados y conclusiones de aporte original y utilidad de la investigación (IMRYC). No escribir en siglas y abreviaturas, no citar referencias ni presentar ninguna información ni conclusión que no figure en el artículo.
            </p>

            <p>
                <b> </b>
                Palabras clave (Keywords), en español e inglés, máximo de cinco palabras clave, en ambos idiomas, debe figurar al final del párrafo de resumen (abstract)  de preferencia no coincidir con el título-
            </p>

            <p>
                <b>&bullet; Introducción (¿Cuál es el problema objeto de estudio y por qué se estudió?):</b>
                Introduzca la investigación describiendo la problemática/ oportunidad y justificación describiendo los antecedentes abordados en la literatura científica más recientes (citados los presupuestos, concepciones y contribuciones). En cada caso, los antecedentes abordados deben ser explícitos en sus aportes de los autores en contribuir a llenar el vació existente a ser abordado. Concluya la sección explicando la importancia y los objetivos del manuscrito.
            </p>

            <p>
                <b>&bullet; Materiales y métodos (¿Cómo y con qué materiales se realizó estudio?): </b>
                descritos en forma concisa los procedimientos metodológicos (lugar de Estudio, tiempo y espacio, material y equipos o instrumentos utilizados). Es decir, describe el enfoque, población, tamaño de muestra, tipo de muestreo, instrumentos de recolección de datos y técnicas de procesamiento y análisis estadístico.
                <br>
                Cuando el estudio refiera o la relación medio ambiental con plantas medicinales, deberá describir los procedimientos de recolección e identificación botánica. Identifique los fármacos y compuestos químicos empleados, con su nombre genérico, sus dosis y vías de administración referenciado la literatura científica correspondiente.
                <br>
                Cuando el objeto de estudio comprometió a seres humanos o animales de experimentación, se debe mencionar que se cumplieron las normas éticas exigidas internacionalmente. Para los estudios en humanos, se debe identificar el comité de ética institucional que aprobó el protocolo en cuyo caso es necesario adjuntar las constancias de aprobación del Comité de Ética y del consentimiento informado del estudio a ser publicado.
            </p>

            <p>
                <b>&bullet; Resultados (¿Qué se encontró en la investigación?): </b>
                en base a los hallazgos de los objetivos describe en tablas, figuras y análisis estadístico (si fuera el caso). En base a los objetivos, si corresponde, consigne títulos y subtítulos los aspectos trascendentales, sin incluir opiniones ni interpretaciones subjetivas evitando repetir la información presentada en el texto con el detalle de tablas o figuras.
            </p>

            <p>
                <b>&bullet; Discusión (¿Qué significa lo encontrado?): </b>
                En base a hallazgos de evidencias del estudio debe ser explicado, comentado y comparado/ discutiendo los resultados de la investigación con otros conocimientos previos las fortalezas y limitaciones, abordados sobre el problema formulado y su resolución de aporte. Se busca defender la validez y confiabilidad del estudio en base a sus objetivos. Todos los argumentos descritos deben estar amparados con literatura científica de últimos tres años preferiblemente incluido el año de postulación.
            </p>

            <p>
                <b>&bullet; Conclusiones (cuál es la posición del investigador respecto a hallazgo o la contribución original del tema investigado): </b>
                Es el resumen de la discusión presentado por el autor (es) de la contribución original de acuerdo a sus los objetivos del problema. Debe ser explicito resaltado la contribución original.
            </p>

            <p>
                Agradecimiento (opcional). A las personas e instituciones que apoyaron a la investigación, colocar el origen del financiamiento recibido.
            </p>

            <p>
                <b>&bullet; Referencias Bibliográficas:</b> Las referencias deben estar relacionados estrictamente con el tema de investigación para respaldar los discursos del manuscrito (preferiblemente de los últimos tres años incluido el año de postulación) en revistas indizadas de alto impacto de conformidad a lo establecido en la política editorial de RIA UNA para la Elaboración de Referencias Bibliográficas. Obligatoriamente, en las referencias deben consignar el DOI ó sitio Web (Link) en todos los extremos, excepcionalmente textos académicos que no están en línea, pueden ser consignados conforme indica APA. Se sugiere usar un gestor de referencias bibliográficos como MENDELEY u otro). Se debe garantizar que, del total de citas referenciadas, como mínimo, al menos el 65% corresponda a los últimos 5 años.
            </p>

            <p>
                <b>Tablas y Figuras:</b>  numeradas en forma correlativa como lo estable el estilo APA insertado en el lugar que corresponde del texto en un formato editable (o de adjuntarse en los programas originales en los que se realizaron, *.jpg, de mínimo 300 dpi). El tamaño máximo real es de 12x18 cms, incluyendo cabecera de tabla y/o pie de figura (en caso de diseños experimentales, adjuntar los datos originales de procesamiento y procesados en excel). Cada una se presentará en un documento aparte de ser el caso, (Tabla 1, Tabla 2,... o figura 1, figura 2…), y deberán ser enviadas sin passwod o claves de acceso que impidan la edición final (una figura se considera gráfica, dibujo o fotografía).
            </p>
